public class TipeDataNumber {

    public static void main(String[] args) {
        // Contoh 1: Tipe data int
        int iniInt = 100;
        System.out.println(iniInt); // Output: 100

        // Contoh 2: Tipe data long
        long iniLong = 10000L;
        System.out.println(iniLong); // Output: 10000

        // Contoh 3: Tipe data float
        float iniFloat = 3.14f;
        System.out.println(iniFloat); // Output: 3.14

        // Contoh 4: Tipe data double
        double iniDouble = 3.14159;
        System.out.println(iniDouble); // Output: 3.14159

        // Contoh 5: Tipe data byte
        byte iniByte = 100;
        System.out.println(iniByte); // Output: 100

        // Contoh 6: Tipe data short
        short iniShort = 100;
        System.out.println(iniShort); // Output: 100

        // Contoh 7: Tipe data char
        char iniChar = 'A';
        System.out.println(iniChar); // Output: A
        
        // Contoh 8: Tipe data boolean
        boolean iniBoolean = true;
        System.out.println(iniBoolean); // Output: true
        
        // Contoh 9: Tipe data String
        String iniString = "Hello, World!";
        System.out.println(iniString); // Output: Hello, World!

        
    }

}
