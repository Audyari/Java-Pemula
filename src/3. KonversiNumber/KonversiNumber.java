
public class KonversiNumber {

    public static void main(String[] args) {

        //number Overflow

        long iniTipeLong = 128;

        System.out.println(iniTipeLong);
       
        byte iniTipeByte = (byte) iniTipeLong;

        System.out.println("Conversi Tipe Byte : " + iniTipeByte);

    }
}