public class TipeDataNumber {

    public static void main(String[] args) {

        System.out.println("");
        
        byte iniTipeByte = 1;
        System.out.println("Tipe Data Byte " + iniTipeByte);

        short iniTipeShort = 2;
        System.out.println(iniTipeShort);

        int iniTipeInt = 4;
        System.out.println(iniTipeInt);

        long iniTipeLong = 8;
        System.out.println(iniTipeLong);


        // Floating Point Number
        float iniTipeFloat = 4;
        System.out.println(iniTipeFloat);

        double iniTipeDouble = 8;
        System.out.println(iniTipeDouble);

        int iniTestHexadesimal = 0xA132B;
        System.out.println(iniTestHexadesimal);

        int iniTestBiner = 0b01010101;
        System.out.println(iniTestBiner);

    }
}